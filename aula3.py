'''
Python = Linguagem de programação
Tipo de tipagem = Dinamica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
'''
print(1234)
# Aspas simples
print('Marcos Tromel')
print(1, 'Marcos "Tromel"')


# Aspas duplas
print("Marcos Tromel")
print(2, "Marcos 'Tromel'")


# Escape
print("Marcos \"Tromel\"")

# r
print(r"Marcos \"Tromel\"")

print('Explícito', 'é', 'melhor-que-implícito.', sep='-')
print('Simples', 'é', 'melhor-que-complexo.', sep='-')

print('Explicito', 'é', 'melhor " do que implicito')
